def test_d2_vault_note_does_not_override_bad_manual_flag() -> None:
    """D2 address notes do not mask explicit bad manual flags."""
    address = "0x64167cd42859f64cff2aa4b63c3175ccef9659dd"
    vault = make_d2_vault(address)
    note = vault.get_notes()

    assert address in D2_VAULT_ADDRESSES
    assert get_vault_special_flags(address) == {VaultFlag.subvault}
    assert is_flagged_vault(address)
    assert note == SUBVAULT


def test_scan_note_normalisation_drops_duplicate_descriptions() -> None:
    """Scan metadata notes do not duplicate exported description fields."""
    description = "Protocol supplied vault description"
    distinct_note = "D2 Finance strategy vault with authoritative source link"

    assert _normalise_scan_note(None, description, None) is None
    assert _normalise_scan_note(description, description, None) is None
    assert _normalise_scan_note(description, None, description) is None
    assert _normalise_scan_note(distinct_note, description, None) == distinct_note
